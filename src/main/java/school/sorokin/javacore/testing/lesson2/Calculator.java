/*Создайте простой класс, например, Calculator, с методомами
multiply(int a, int b), который возвращает произведение двух чисел.
sum (int a, int b), который суммирует два числа*/
public class Calculator {
    public  static int multiply(int a, int b) {
        return a * b;
    }

    public static int add(int a, int b) {
        return a + b;
    }

}
